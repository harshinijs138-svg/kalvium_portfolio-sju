function createStudentCard(student) {
  const card = document.createElement('div');
  card.className = 'student-card';
  card.dataset.squad = student.squad;
  card.dataset.name = student.name.toLowerCase();

  const initials = getInitials(student.name);
  const imagePath = getStudentImage(student.name);

  const cardInner = document.createElement('div');
  cardInner.className = 'student-card-inner';

  const imageContainer = document.createElement('div');
  imageContainer.className = 'student-image-container';

  if (imagePath) {
    const img = document.createElement('img');
    img.src = imagePath;
    img.alt = student.name;
    img.className = 'student-image';
    img.onerror = function() {
      this.parentElement.innerHTML = `<div class="student-avatar-fallback">${initials}</div>`;
    };
    imageContainer.appendChild(img);
  } else {
    imageContainer.innerHTML = `<div class="student-avatar-fallback">${initials}</div>`;
  }

  const bioOverlay = document.createElement('div');
  bioOverlay.className = 'student-bio-overlay';
  bioOverlay.innerHTML = `<p>${student.bio}</p>`;
  imageContainer.appendChild(bioOverlay);

  const studentInfo = document.createElement('div');
  studentInfo.className = 'student-info';
  studentInfo.innerHTML = `
    <h3 class="student-name">${student.name}</h3>
    <p class="student-email">${student.email}</p>
    <div class="student-links">
      ${student.github ? `<a href="${student.github}" target="_blank" class="student-link" title="GitHub"><i class="fab fa-github"></i></a>` : ''}
      ${student.linkedin ? `<a href="${student.linkedin}" target="_blank" class="student-link" title="LinkedIn"><i class="fab fa-linkedin"></i></a>` : ''}
    </div>
  `;

  cardInner.appendChild(imageContainer);
  cardInner.appendChild(studentInfo);
  card.appendChild(cardInner);

  card.addEventListener('click', function(e) {
    if (!e.target.closest('.student-link')) {
      openModal(student);
    }
  });

  return card;
}

function renderStudents(filter = 'all', searchTerm = '') {
  const grid = document.getElementById('students-grid');
  grid.innerHTML = '';

  const filteredStudents = studentsData.filter(student => {
    const matchesFilter = filter === 'all' || student.squad === filter;
    const matchesSearch = student.name.toLowerCase().includes(searchTerm.toLowerCase());
    return matchesFilter && matchesSearch;
  });

  filteredStudents.forEach(student => {
    grid.appendChild(createStudentCard(student));
  });
}

